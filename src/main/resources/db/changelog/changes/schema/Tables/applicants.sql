CREATE TABLE IF NOT EXISTS public.applicants
(
    applicant_id bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 9223372036854775807 CACHE 1 ),
    first_name character varying(255) COLLATE pg_catalog."default" NOT NULL,
    has_applied boolean NOT NULL,
    is_selected boolean NOT NULL,
    last_name character varying(255) COLLATE pg_catalog."default" NOT NULL,
    registration_timestamp timestamp(6) with time zone NOT NULL,
    timezone character varying(255) COLLATE pg_catalog."default" NOT NULL,
    project_id bigint NOT NULL,
    team_id bigint NOT NULL,
    user_id bigint,
    CONSTRAINT PK_Applicants PRIMARY KEY (applicant_id),
    CONSTRAINT FK_applicants_team_id FOREIGN KEY (team_id)
        REFERENCES public.teams (team_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT FK_Applicants_project_id FOREIGN KEY (project_id)
        REFERENCES public.projects (project_id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
)